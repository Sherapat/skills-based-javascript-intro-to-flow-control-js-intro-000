function basicTeenager(age) {
  if (age == 13 && 19) {
  return("You are a teenager!");
}
}


function teenager(age) {
if (age == 13 && 19) {
  return("You are a teenager!");
} else {
  return("You are not a teenager");
  }
}

function ageChecker(age) {
if (age == 13 && 19) {
  return("You are a teenager!");
} else if (age > 19) {
return("You are a grownup");
} else {
return("You are a kid");
  }
}

function ternaryTeenager(age) {
   return("You are not a teenager");
  if (age == 13 && 19) {
   return("You are a teenager");
}
}

